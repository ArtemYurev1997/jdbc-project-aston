package org.aston.jdbc.enums;

public enum Role {
    ADMIN("Admin"),
    CLIENT("Client");

    private String role;

    Role(String role) {
        this.role = role;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    @Override
    public String toString() {
        return role;
    }
}
